package Inheritance;

public class InheritanceDocs {
	/*
	 * ================================ Inheritance ================================
	 *  # use to achieve code re-usability
	 *  # process of accessing properties and methods of parent/super/base class
	 *    to child/sub/derived class is known as inheritance.
	 *    
	 *  # Also called IS-A relationship
	 *  # It's a Generalization to  Specialization process.
	 *  # 'extends' keyword is used in inheritance
	 *  # we can't inherit private properties,private methods and constructors.
	 *  # we can  inherit static properties and methods.
	 *  # we can inherit only one class at a time.
	 *  
	 *    syntax :
	 *    
	 *     class child_class_name    extends  parent_class_name{
	 *     
	 *         //properties  : parent + child
	 *           
	 *         //methods     : parent + child
	 *     }
	 */

}
