package oops;

public class OOPDocs {
	
	/*
	 * ===================== OOP(Object Oriented Programming) =====================
	 *    1> Class & Object
	 *    2> Encapsulation
	 *    3> Inheritance
	 *    4> Polymorphism
	 *    5> Abstraction
	 *      a> Abstract class
	 *      b> Interface
	 *      
	 *      ================ Class ============================
	 *      -> use to represent real world entity/things.
	 *      -> blue print/template of object.
	 *      -> collection/group of objects having similar behaviors.
	 *      -> class contains properties  and methods.
	 *      -> its a user defined/reference data type.
	 *      
	 *      syntax :
	 *        class  class_name{
	 *        
	 *                //properties 
	 *                
	 *                //methods
	 *        }
	 *        
	 *    ===================== Object/Instance ==============================
	 *    -> implementation of class.
	 *    -> instance of class
	 *    -> object contains instance variable and instance methods.
	 *    -> object allocate memory when it created.
	 *    
	 *    syntax :
	 *      class_name   obj_name = new class_name();
	 *               OR
	 *      new class_name();
	 */

}
